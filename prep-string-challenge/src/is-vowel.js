function isVowel(string) {
  if (
    string === 'a' ||
    string === 'e' ||
    string === 'i' ||
    string === 'o' ||
    string === 'u' ||
    string === 'A' ||
    string === 'E' ||
    string === 'I' ||
    string === 'O' ||
    string === 'U'
  ) {
    return true;
  } else return false;
}
